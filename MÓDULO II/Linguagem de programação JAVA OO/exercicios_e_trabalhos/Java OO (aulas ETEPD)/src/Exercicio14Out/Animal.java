public class Animal {
	String Nome, Ambiente,Comprimento, Cor;
	double Velocidade;
	int Patas;
	
	public Animal (String nome, String ambiente, String comprimento, String cor, double velocidade,int patas) {
		this.Nome = nome;
		this.Ambiente = ambiente;
		this.Comprimento=comprimento;
		this.Cor = cor;
		this.Velocidade = velocidade;
		this.Patas = patas;
		
	}
}
